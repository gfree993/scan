def run_scan():
    tickers = get_350_tickers()
    results = []
    print(f"🔍 총 {len(tickers)}개 종목 분석 시작 (약 5~10분 소요)...")
    
    for i, symbol in enumerate(tickers):
        try:
            # 과도한 요청 방지 (0.2초 간격)
            time.sleep(0.2)
            
            df = yf.download(symbol, period="200d", progress=False)
            if df.empty or len(df) < 120: continue

            # 지표 계산
            df['MA20'] = df['Close'].rolling(20).mean()
            df['MA120'] = df['Close'].rolling(120).mean()
            df['Vol_MA5'] = df['Volume'].rolling(5).mean()
            
            curr = df.iloc[-1]
            prev = df.iloc[-2]
            
            # --- 전략 1: 발목 턴어라운드 (바닥 탈출) ---
            # 어제까지 장기선 아래 있다가 오늘 강력한 거래량과 함께 뚫은 종목
            is_ankle = (prev['Close'] < prev['MA120']) and (curr['Close'] > curr['MA120']) and (curr['Volume'] > df['Volume'].mean() * 1.8)
            
            # --- 전략 2: 침착해식 수급 눌림목 (종가베팅) ---
            # 상승 추세(20일선 위)에서 거래량이 전일 대비 혹은 평균 대비 확 죽으면서 지지받는 종목
            is_pullback = (curr['Close'] > curr['MA20']) and \
                          (abs(curr['Close'] - curr['MA20'])/curr['MA20'] < 0.02) and \
                          (curr['Volume'] < prev['Volume'] * 0.6) and \
                          (curr['Volume'] < df['Volume'].mean() * 0.7)

            if is_ankle:
                results.append(f"🚀 [발목] {symbol}")
            elif is_pullback:
                results.append(f"⚓ [종베] {symbol}")
                
        except Exception:
            continue
            
    return "\n".join(results)

